mood = input("How do you feel today? ")

if mood == "happy":
    print("It's great to see you happy! ")
elif mood == "sad":
    print("I hope your day gets better! ")
else:
    print("I'm sorry, I don't understand. ")

# I started with asking the user how they felt today using 'input'. 
# I then used an if statement to check if they were happy or sad. 
# I then printed a response based on their mood. 
# I used an else statement to catch any other mood they may have entered.